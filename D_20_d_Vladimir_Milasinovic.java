package DomaciOOP3;

public class Radnik {
    int ID;
    String nazivRadnika;
    int godineIskustva;
    int plata;

    public Radnik(int ID, String nazivRadnika, int godineIskustva, int plata) {
        this.ID = ID;
        this.nazivRadnika = nazivRadnika;
        this.godineIskustva = godineIskustva;
        this.plata = plata;
    }
}
